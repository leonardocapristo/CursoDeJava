package exemplos;

public class Conversao {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 2;
		
		double resultado = a/b;
		
		System.out.println(resultado);
		
		
		//para converter tem que que colocar o (double)
		
		 a = 5;
		 b = 2;
		
		 resultado = (double) a/b;
		
		System.out.println(resultado);
	}
}
